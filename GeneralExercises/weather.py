
weather = input('What is the weather like today?\n')

if weather == 'rainy':
    print('Grab your umbrella!')
elif weather == 'sunny':
    print('It is a beautiful day!')
elif weather == 'snowy':
    print('Wear your boots!')
elif weather == 'cloudy':
    print('It might rain, take your umbrella!')
else:
    print('Let\'s stay inside!')