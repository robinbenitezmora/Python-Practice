


noun = input('Enter a noun: ')
verb = input('Enter a verb: ')
adjective = input('Enter an adjective: ')
adverb = input('Enter an adverb: ')

def madlibs(noun, verb, adjective, adverb):
    print(f'Do you {verb} your {adjective} {noun} {adverb}? That\'s hilarious!')
    print(f'The {adjective} {noun} {verb}s {adverb} over the lazy dog.')
    print(f'The {noun} {adverb} {verb}s up {adjective} Joe\'s turtle.')

madlibs(noun, verb, adjective, adverb)